- (CAGradientLayer *)gradientLayer
{
    if(!_gradientLayer)
    {
        _gradientLayer = [CAGradientLayer layer];  // 设置渐变效果
        _gradientLayer.borderWidth = 0;
        
        _gradientLayer.colors = [NSArray arrayWithObjects:
                                 (id)[[UIColor blackColor] CGColor],
                                 (id)[[UIColor clearColor] CGColor], nil,nil];
        _gradientLayer.startPoint = CGPointMake(0.2, 0.2);
        _gradientLayer.endPoint = CGPointMake(0.2, 1.0);
        
        [self.topBar.layer insertSublayer:_gradientLayer atIndex:0];
    }
    return _gradientLayer;
}



- (UIActivityIndicatorView *)indicatorView
{
    if (!_indicatorView) {
        _indicatorView = [[UIActivityIndicatorView alloc] initWithActivityIndicatorStyle:UIActivityIndicatorViewStyleWhite];
        [_indicatorView stopAnimating];
    }
    return _indicatorView;
}

- (VideoLoadingView *)loadingView
{
    if(!_loadingView)
    {
        _loadingView = [[VideoLoadingView alloc]initWithFrame:CGRectMake(0, 0, 30, 30)];
    }
    return _loadingView;
}

